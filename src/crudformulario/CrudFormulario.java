    private void btnCargarImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarImagenActionPerformed
        fc = new JFileChooser();
        fc.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        FileNameExtensionFilter filtro = new FileNameExtensionFilter(".JPG", "jpg");
        fc.setFileFilter(filtro);
        int seleccion = fc.showOpenDialog(this);
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            File fichero = fc.getSelectedFile();
            String ruta = fichero.getAbsolutePath();
            nombreArchivo = fichero.getName();
            destino = "C:/APPJAVA/IMG/";
            nombre1 = destino + "" + nombreArchivo;
            File folder = new File(destino);
            if (!folder.exists()) {
                folder.mkdirs();
            }
            copyFile_Java(ruta, nombre1);
            
            int x = jpFoto.getWidth();
            int y = jpFoto.getHeight();
            Imagen img = new Imagen(x, y, ruta);
            jpFoto.add(img);
            jpFoto.repaint();
        }
    }//GEN-LAST:event_btnCargarImagenActionPerformed
    public static void copyFile_Java(String origen, String destino){
        try {
            Path FROM = Paths.get(origen);
            Path TO = Paths.get(destino);
            CopyOption[] options = new CopyOption[]{
                StandardCopyOption.REPLACE_EXISTING,
                StandardCopyOption.COPY_ATTRIBUTES
            };
            Files.copy(FROM, TO, options);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al Guardar Imagen");
            System.err.println(e.toString());
        }
    
    }
