    public void tabla (String valor){
        try {
            String titulos[] = {"Codigo","Nombre","Direccion","Telefono"};
            String registros[] = new String[4];
            miModel = new DefaultTableModel(null, titulos);
            Conexion conectar = new Conexion();
            Connection miConexion = conectar.conexion();
            String instruccionSQL = "select * from alumno where nombre LIKE '%"+valor+"%'";
            PreparedStatement ps = miConexion.prepareStatement(instruccionSQL);
            ResultSet rs = ps.executeQuery(instruccionSQL);
            while (rs.next()) {
                registros[0] = rs.getString("codigo");
                registros[1] = rs.getString("nombre");
                registros[2] = rs.getString("direccion");
                registros[3] = rs.getString("telefono");
                miModel.addRow(registros);
            }
            miTabla.setModel(miModel);
            
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    
    }
    
